    private void btnCalcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcActionPerformed
        float numerador = Float.parseFloat(txtNum.getText().replace(",", "."));
        float denominador = Float.parseFloat(txtDen.getText().replace(",", "."));
        
        float div = numerador / denominador;
        float raiz = (float) Math.sqrt(numerador);
        
        lblDivisao.setText(Float.toString(div).format("%.1f", div));
        lblRaiz.setText(Float.toString(raiz).format("%.1f", raiz));
    }//GEN-LAST:event_btnCalcActionPerformed

